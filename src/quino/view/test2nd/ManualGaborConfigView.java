    @SuppressWarnings("static-access")
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()
                    || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()
                    || jTextField6.getText().isEmpty()
                    || jTextField7.getText().isEmpty()) {
                throw new Exception("No puede dejar datos en blanco");
            }

            int cantEnsayos, intensidadMedia, radio1, radio2;
            float contraste;
            double gaussianStd, ppi, ppi2, pVel1, ft;
            try {
                cantEnsayos = Integer.parseInt(jTextField1.getText());
                contraste = Float.parseFloat(jTextField2.getText());
                intensidadMedia = Integer.parseInt(jTextField3.getText());
                gaussianStd = Double.parseDouble(jTextField4.getText());
                radio1 = Integer.parseInt(jTextField6.getText());
                radio2 = Integer.parseInt(jTextField7.getText());
                if (intensidadMedia < 0 || intensidadMedia > 255) {
                    throw new Exception();
                }               
                //ppi = Integer.parseInt(jTextField5.getText());
                //ppi2 = Integer.parseInt(jTextField8.getText());
                
                double value = Double.parseDouble(jComboBox1.getModel().getSelectedItem().toString());
                //ppi = QuinoTools.getPPiXFrecuenciaEspacial(value);
                ppi = value;
                
                value = Double.parseDouble(jComboBox2.getModel().getSelectedItem().toString());
                //ppi2 = QuinoTools.getPPiXFrecuenciaEspacial(value);
                ppi2 = value;
                
                pVel1 = Double.parseDouble(jComboBox3.getModel().getSelectedItem().toString());
                ft = Double.parseDouble(jComboBox4.getModel().getSelectedItem().toString());
                
            } catch (Exception e) {
                throw new Exception("El formato de los datos no es correcto");
            }

            if (radio1 >= radio2) {
                throw new Exception("El radio interior debe ser menor que el radio exterior");
            }

            ConfigEnsayoGabor ceg = new ConfigEnsayoGabor(direccion, ppi, contraste,
                    (byte) intensidadMedia, gaussianStd, radio1, radio2, ppi2);
            ceg.setFrecuenciaMuestreo(ft);
            ceg.setVelocidadPrimaria(new Velocidad(pVel1));
            
            parent.setConf(ceg);
            parent.setPrueba(new PruebaGabor(((ConfigEnsayoGabor) parent.getConf()), cantEnsayos));

            GaborTestView gtv = new GaborTestView(parent, true, false);
            gtv.setVisible(true);

            setVisible(false);
            dispose();

        } catch (Exception e) {
            ErrorDialog err = new ErrorDialog(parent, true, e.getMessage());
            err.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

