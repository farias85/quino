    /**
     * Creates new form GaborTestView
     *
     * @param parent
     * @param modal
     * @param practica
     */
    public GaborTestView(PrincipalView parent, boolean modal, boolean practica) {
        super(parent, modal);

        this.parent = parent;
        initComponents();
        setLocationRelativeTo(null);

        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        getContentPane().setBackground(Color.BLACK);
        jPanel1.setBackground(Color.BLACK);

        TimerTask task = new GaborTimer(parent.getPrueba(), practica, jPanel1, this);

        //El par√°metro de frecuencia temporal debe ser igual en cada uno de los ensayos
        Ensayo ensayo = parent.getPrueba().getEnsayos().get(0);

        if (ensayo.getConfiguracion() instanceof ConfigEnsayoGabor) {
            ConfigEnsayoGabor configEnsayo = ((ConfigEnsayoGabor) ensayo.getConfiguracion());
            long periodo = (long) QuinoTools.getPeriodoXFrecuencia(configEnsayo.getFrecuenciaMuestreo());
            Timer ti = new Timer();
            ti.scheduleAtFixedRate(task, 0, periodo);
        } else {
            System.err.println("quino.view.test2nd.GaborTestView.<init>() no ha enviado una configuracion ConfigEnsayoGabor");
        }
    }

    public PrincipalView getParentView() {
        return parent;
    }

